package xin.liu;

public class Discussion {
	private int discussionID;
	private int meetingID;
	private String discussionType;
	private int questionnaireID;
	private int swotID;
	private int competeHypothesID;
	private String discussionName;
	public int getDiscussionID() {
		return discussionID;
	}
	public void setDiscussionID(int discussionID) {
		this.discussionID = discussionID;
	}
	public int getMeetingID() {
		return meetingID;
	}
	public void setMeetingID(int meetingID) {
		this.meetingID = meetingID;
	}
	public String getDiscussionType() {
		return discussionType;
	}
	public void setDiscussionType(String discussionType) {
		this.discussionType = discussionType;
	}
	public int getQuestionnaireID() {
		return questionnaireID;
	}
	public void setQuestionnaireID(int questionnaireID) {
		this.questionnaireID = questionnaireID;
	}
	public int getSwotID() {
		return swotID;
	}
	public void setSwotID(int swotID) {
		this.swotID = swotID;
	}
	public int getCompeteHypothesID() {
		return competeHypothesID;
	}
	public void setCompeteHypothesID(int competeHypothesID) {
		this.competeHypothesID = competeHypothesID;
	}
	public String getDiscussionName() {
		return discussionName;
	}
	public void setDiscussionName(String discussionName) {
		this.discussionName = discussionName;
	}
}
